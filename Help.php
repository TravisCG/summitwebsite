<p class="western" style="margin-bottom: 0in; line-height: 100%"><font face="Arial, serif"><span lang="en-US">The
diagrams of the motif view cumulatively represent the statistical
data of all occupying ChIP-seq experiments (occurrence frequency,
average/median distance related to the motif, and distance standard
deviation) on all instances (consensus motif set) of an adjusted
