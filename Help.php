<font face="Arial, serif"><span lang="en-US">To visualize the data,
we need to set the options. We recommend that you set the motif first
in the dropdown box below “Set a motif”. After you choose a motif
of interest, you can set which experiments you want to investigate.
(You can select them using the dropdown menus, or you can navigate to
pair shift view comparison from motif view after you highlighted the
dots (experimental data) that you are interested in. You can read
about this in ”Details”, found in the Motif view Help section. To
select the ChIP-seq data of interest, set the attributes of the
experiment in the dropdown boxes from right to left: in the first box
select the cell type, in the second you can pick the antibody, and in
the third box you can choose by the name of the experiment. The
experiment name is related to the experimental attributes: the tissue
type and the origin of the cell type, the target protein name, and
the SRA experiment ID. When you set all the parameters, click on the
“Resend Data” button. Following the page refresh, the updated
data will be displayed. The minimum and maximum values of the axes
are configurable as well in the text boxes below the diagram. A
rolling mean with a 5 bp frame was applied to smooth the frequency
curves.</span></font></p>
